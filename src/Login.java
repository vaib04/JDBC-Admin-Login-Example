    static boolean loginCheck(String uname,String pass) throws SQLException {
        database=new Database(dbUrl.URL);
        sql="select * from account where user=? and password=?";
        ps=database.conn.prepareStatement(sql);
        ps.setString(1, uname);
        ps.setString(2, pass);
        rs=ps.executeQuery();
        if(!rs.next()) {
            return false;    //no such user
        }
        return true;   //user exists
    }
    
    static String accStatus(String uname) {
        database=new Database(dbUrl.URL);
        String x=null;
        sql="select temp from account where user=?";
        try {
            ps=database.conn.prepareStatement(sql);
            ps.setString(1,uname);
            rs=ps.executeQuery();
            rs.next();
            x=rs.getString("temp");
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        return x;
    }
    
