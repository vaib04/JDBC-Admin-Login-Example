    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        // TODO add your handling code here:
      
        String uname=jTextField1.getText();
        char tmpPass[]=jPasswordField1.getPassword();
        String pass=String.copyValueOf(tmpPass);
        if(uname.equals("") || pass.equals("")) {
            System.out.println("username and password are compulsary");
        }
        else {
            try {
                if(loginCheck(uname, pass)) {
                    System.out.println("Trying to login: "+uname);
                    if(accStatus(uname).equals("d")) {
                        JOptionPane.showMessageDialog(Login.this, "Account not enabled by admin", "Account status", JOptionPane.ERROR_MESSAGE);
                    }
                    else if(accStatus(uname).equals("f")) {
                        JOptionPane.showMessageDialog(Login.this, "Account frozen by admin", "Account status", JOptionPane.ERROR_MESSAGE);
                    }
                    else if(accStatus(uname).equals("x")) {
                        AL=new AdminLogin();
                        AL.setVisible(true);
                        Login.this.dispose();
                    }
                    else {
                        UL=new UserLogin(uname);
                        UL.setVisible(true);
                        Login.this.dispose();
                    }
                }
                else {
                    JOptionPane.showMessageDialog(Login.this, "Wrong credentials", "Login failed", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        jTextField1.setText("");
        jPasswordField1.setText("");
    }//GEN-LAST:event_jButtonLoginActionPerformed

